#Titulo y lectura de datos
print("Programa que calcula el area de un triangulo")
base = float(input("Ingrese la base: "))


#Procesos
altura = base**2
area = (base * altura)/2

#Presentacion de los resultados
print("El area del triangulo con la base ingresada es:", area, "m2")
